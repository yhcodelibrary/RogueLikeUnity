    public static BaseItem GetTypeItemRandom(int intype, ItemType itype, bool isRandomValue)
    {
        //TableItemIncidenceData[] datas = Table.Where(i => i.IncidenceType == intype && i.IType == itype).ToArray();
        TableItemIncidenceData[] datas = Array.FindAll(GetTable(intype), i => i.IncidenceType == intype && i.IType == itype);

        if (datas.Length == 0)
        {
            return null;
        }
        TableItemIncidenceData first = datas[0];

        foreach (TableItemIncidenceData d in datas)
        {
            d.StartPoint -= first.StartPoint;
            d.EndPoint -= first.StartPoint;
        }
        TableItemIncidenceData last = datas.Last();

        //uintの乱数を取得
        uint rand = CommonFunction.GetRandomUInt32(last.EndPoint);
        TableItemIncidenceData data = Array.Find(datas, i => i.StartPoint <= rand && rand <= i.EndPoint);
        //datas.Where(i => i.StartPoint <= rand && rand <= i.EndPoint).FirstOrDefault();
        return GetItemObjNo(data.IType, data.ObjNo, isRandomValue);
    }

    public static BaseItem GetItem(int intype, uint rand,bool isRandomValue)
    {
        //TableItemIncidenceData data = Table.Where(i => i.IncidenceType ==intype && i.StartPoint <= rand && rand <= i.EndPoint).FirstOrDefault();
        TableItemIncidenceData data;
        data = Array.Find(GetTable(intype), i => i.IncidenceType == intype && i.StartPoint <= rand && rand <= i.EndPoint);
        
        if (CommonFunction.IsNull(data) == true)
        {
            return null;
        }
        return GetItemObjNo(data.IType, data.ObjNo, isRandomValue);
    }

    private static TableItemIncidenceData[] GetTable(int intype)
    {

        if (9 <= intype && intype <= 13)
        {
            return Table9to13;
