    public static BaseItem GetItemObjNo(ItemType itype,long objNo,bool isRandomValue)
    {
        BaseItem item = null;
        switch (itype)
        {
            case ItemType.Weapon:
                item = TableWeapon.GetItem(objNo, isRandomValue);
                break;
            case ItemType.Shield:
                item = TableShield.GetItem(objNo, isRandomValue);
                break;
